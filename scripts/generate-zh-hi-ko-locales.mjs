function deepTranslate(obj, map) {
  if (typeof obj === 'string') {
    return map[obj] ?? obj;
  }
  if (Array.isArray(obj)) {
    return obj.map((item) => deepTranslate(item, map));
  }
  if (obj && typeof obj === 'object') {
    const out = {};
    for (const [k, v] of Object.entries(obj)) {
      out[k] = deepTranslate(v, map);
    }
    return out;
  }
  return obj;
}

